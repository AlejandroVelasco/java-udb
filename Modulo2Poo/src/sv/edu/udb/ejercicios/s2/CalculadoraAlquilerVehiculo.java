    private void agregarParametrosIniciales(){
     DefaultListModel<Marca> lstMarca = new DefaultListModel<>();
     lstMarca.addElement(bmw);
     lstMarca.addElement(toyota);
     lstMarca.addElement(nissan);
     lstMarca.addElement(hyundai);
     this.lstMarcas.setModel(lstMarca); 
    }
    
    private Extras calcularExtras(){
     Extras extras = new Extras();
     extras.setEstereo(this.chkEstereo.isSelected());
     extras.setSunroof(this.chkSunroof.isSelected());
     extras.setRines(this.chkRines.isSelected());
     return extras;
    }
    
    private Automovil agregarAutomovil(){
        Automovil auto = new Automovil();
        Transmision transmision;
        if(this.rbtAutomatico.isSelected()){
            transmision = automatica;
        }else{
            transmision = manual;
        }
        auto.setMarca(this.lstMarcas.getSelectedValue());
        auto.setModelo((Modelo)this.cbxModelos.getSelectedItem());
        auto.setTransmision(transmision);
        return auto;
    }
    
