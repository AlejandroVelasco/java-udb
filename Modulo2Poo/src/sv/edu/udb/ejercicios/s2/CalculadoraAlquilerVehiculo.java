    private void lstMarcasValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_lstMarcasValueChanged
        // TODO add your handling code here:
            DefaultComboBoxModel<Modelo> cbxModel = new DefaultComboBoxModel<Modelo>();
            Marca marca = this.lstMarcas.getSelectedValue();
            if(marca == bmw){
                cbxModel.addElement(bmw328);
                cbxModel.addElement(bmw507);
            }
            if(marca == toyota){
                cbxModel.addElement(corolla);
                cbxModel.addElement(yaris);
            }
            if(marca == nissan){
                cbxModel.addElement(sentra);
                cbxModel.addElement(versa);
            }
            if(marca == hyundai){
                cbxModel.addElement(elantra);
                cbxModel.addElement(accent);
            }
            this.cbxModelos.setModel(cbxModel);
    }//GEN-LAST:event_lstMarcasValueChanged

