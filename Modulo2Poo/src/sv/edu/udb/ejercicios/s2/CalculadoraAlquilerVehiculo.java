        this.lblTotalAuto.setText("$" + cotizacion.getTotalAuto());
        this.lblExtras.setText("$" + cotizacion.getTotalExtras());
        this.lblDias.setText(""+dias);
        this.lblSubtotal.setText("$" + cotizacion.getSubtotal());
        this.lblIVA.setText("$" + cotizacion.getTotalIva());
        this.lblTotalPagar.setText("$" + cotizacion.getTotalPagar());
