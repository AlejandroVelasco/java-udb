    private void calcularTotal(int operacion){
        try{
            double numero1 = Double.parseDouble(this.txtNumero1.getText());
            double numero2 = Double.parseDouble(this.txtNumero2.getText());
            double resultado = 0;
            if(operacion == 0){
                lblSigno.setText("+");
                resultado = numero1+numero2;
            }
            if(operacion == 1){
                lblSigno.setText("-");
                resultado = numero1-numero2;
            }
            if(operacion == 2){
                lblSigno.setText("*");
                resultado = numero1*numero2;
            }
            if(operacion == 3){
                lblSigno.setText("/");
                resultado = numero1/numero2;
            }
            txtResultado.setText("" + df.format(resultado));
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, e.toString(), "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }
    
