    private void btnAceptarRetiroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarRetiroActionPerformed
        // TODO add your handling code here:
        BigDecimal montoRetirar = new BigDecimal(this.spnMontoRetiro.getValue().toString());
        Cuenta cuenta = (Cuenta) this.cbxCuentas.getSelectedItem();
        if (montoRetirar.compareTo(cuenta.getSaldo()) == 1) {
            JOptionPane.showMessageDialog(null, "El monto a retirar sobrepasa el saldo de la cuenta");
        } else {
            MovimientoControl.guardarRetiro(cuenta, montoRetirar);
            JOptionPane.showMessageDialog(null, "RETIRO REALIZADO");
        }
        cargarComboCuentas();
    }//GEN-LAST:event_btnAceptarRetiroActionPerformed

