    public void verCuentas() {
        DefaultListModel modelo = new DefaultListModel();
        lstCuentas = new ArrayList<>();
        lstCuentas = CuentasControl.listarCuentasUsuario(usuario);
        for (Cuenta cuentaActual : lstCuentas) {  //foreach          for(Clase objeto : lstClase)
            modelo.addElement(cuentaActual);
        }
        lstCuentasUsuario.setModel(modelo);
    }

    public void cargarComboCuentas() {
        DefaultComboBoxModel modelo = new DefaultComboBoxModel();
        lstCuentas = new ArrayList<>();
        lstCuentas = CuentasControl.listarCuentasUsuario(usuario);
        for (Cuenta cuentaActual : lstCuentas) {
            modelo.addElement(cuentaActual);
        }
        cbxCuentas.setModel(modelo);
        cbxCuentasAbono.setModel(modelo);
    }

