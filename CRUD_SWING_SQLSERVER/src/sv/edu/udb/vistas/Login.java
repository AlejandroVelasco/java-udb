    private void txtIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtIngresarActionPerformed
        // TODO add your handling code here:
        String usuario = this.txtUsuario.getText();
        String pass = String.valueOf(this.txtPwd.getPassword());
        Usuario usuarioValido = LoginControl.validarUsuario(usuario, pass);
        if(usuarioValido.getIdEstado() == 1){
            CatalogoAlumno catalogoAlumno = new CatalogoAlumno();
            catalogoAlumno.setVisible(true);
            this.dispose();
        }else if(usuarioValido.getIdEstado() == 2){
            JOptionPane.showMessageDialog(this, "Usuario inactivo");
        }else{
            JOptionPane.showMessageDialog(this, "Usuario no registrado");
        }
        
    }//GEN-LAST:event_txtIngresarActionPerformed

