    private void mniBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mniBorrarActionPerformed
        // TODO add your handling code here:
        int filaSeleccionada = tblAlumnos.getSelectedRow();
        
        if(filaSeleccionada != 1){
            int carnet = Integer.parseInt(this.tblAlumnos.getModel().getValueAt(filaSeleccionada,0).toString());
            int confirm = JOptionPane.showConfirmDialog(null, "Seguro que desea eliminar", "Borrar", JOptionPane.YES_NO_OPTION);
            if (confirm==0){
                AlumnoControl.eliminarAlumno(carnet);
                this.cargarTabla("");
            }
        }else{
            JOptionPane.showMessageDialog(null, "Seleccione un fila");
        }
    }//GEN-LAST:event_mniBorrarActionPerformed

