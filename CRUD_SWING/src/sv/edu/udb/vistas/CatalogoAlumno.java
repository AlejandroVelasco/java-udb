    public Alumno capturarDatos(){
        int carnet = 0;
        if(modificar)
            carnet = Integer.parseInt(this.txtCarnet.getText());
        String nombres = this.txtNombres.getText();
        String apellidos = this.txtApellidos.getText();
        Alumno alumno = new Alumno(carnet, nombres, apellidos);
        return alumno;
    }

