    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        if(modificar){
            AlumnoControl.modificarAlumno(capturarDatos());
            modificar=false;
        }else{
            AlumnoControl.agregarAlumno(capturarDatos());
        }
        this.cargarTabla("");
        Utils.limpiarPanel(this.pnlAlumno);
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void mniModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mniModificarActionPerformed
        // TODO add your handling code here:
        int filaSeleccionada = tblAlumnos.getSelectedRow();
        
        if(filaSeleccionada != 1){
            int carnet = Integer.parseInt(this.tblAlumnos.getModel().getValueAt(filaSeleccionada,0).toString());
            String nombres =this.tblAlumnos.getModel().getValueAt(filaSeleccionada,1).toString();
            String apellidos = this.tblAlumnos.getModel().getValueAt(filaSeleccionada,2).toString();
            this.txtCarnet.setText(String.valueOf(carnet));
            this.txtNombres.setText(nombres);
            this.txtApellidos.setText(apellidos);
            modificar = true;
        }else{
            JOptionPane.showMessageDialog(null, "Seleccione un fila");
        }
    }//GEN-LAST:event_mniModificarActionPerformed

